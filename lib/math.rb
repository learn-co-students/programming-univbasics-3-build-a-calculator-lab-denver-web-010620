def addition(num1, num2)
sum = num1 + num2
sum
end

def subtraction(num1, num2)
subtract = num1 - num2
subtract
end

def division(num1, num2)
divide = num1 / num2
divide
end

def multiplication(num1, num2)
times = num1 * num2
times
end

def modulo(num1, num2)
modo = num1 % num2
modo
end

def square_root(num)
power = Math.sqrt(num)
power
end